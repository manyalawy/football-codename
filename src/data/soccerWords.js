// Function to get all words as a flat array
export const getAllSoccerWords = () => {
  const allWords = [];
  Object.values(soccerWords).forEach(category => {
    allWords.push(...category);
  });
  return allWords;
};

// Function to get random words for the game
export const getRandomSoccerWords = (count = 25) => {
  const allWords = getAllSoccerWords();
  const shuffled = [...allWords].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Function to get words by category
export const getWordsByCategory = (category) => {
  return soccerWords[category] || [];
};

// Function to get a balanced mix of words from different categories
export const getBalancedSoccerWords = (count = 25) => {
  const categories = Object.keys(soccerWords);
  const wordsPerCategory = Math.floor(count / categories.length);
  const remainder = count % categories.length;
  
  let selectedWords = [];
  
  categories.forEach((category, index) => {
    const categoryWords = soccerWords[category];
    const shuffled = [...categoryWords].sort(() => 0.5 - Math.random());
    const take = wordsPerCategory + (index < remainder ? 1 : 0);
    selectedWords.push(...shuffled.slice(0, take));
  });
  
  // Shuffle the final array
  return selectedWords.sort(() => 0.5 - Math.random());
};
